package day2;

public class Demo {

	public static void main(String[] args) {

		// area of rectangle:- area =l*b

		int l = 10;

		int b = 6;

		int area = l * b;

		System.out.println("The area of the rectangle is=:" + area);

		// area of circle :- 3.14 , r = d/2
		// d = 15 , r =7.5

		float r = 7.2f;

		double Carea = 3.14 * r * r;

		System.out.println("The area of the circle is =:" + Carea);

	}

}
